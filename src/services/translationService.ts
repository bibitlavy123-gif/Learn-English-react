export const translateWord = (englishWord: string): string => {
  const word = englishWord.toLowerCase().trim();
  return WORD_DICTIONARY[word] || `[${word}]`; // Return word in brackets if not found
};

export const translateWords = (englishWords: string[]): Array<[string, string]> => {
  return englishWords.map(word => [word, translateWord(word)]);
};

